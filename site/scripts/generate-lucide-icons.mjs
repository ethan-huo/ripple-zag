import { icons } from "lucide"
import { mkdir, readFile, writeFile } from "node:fs/promises"
import { dirname, relative, resolve } from "node:path"
import { fileURLToPath } from "node:url"

const scriptDir = dirname(fileURLToPath(import.meta.url))
const siteRoot = resolve(scriptDir, "..")
const sourceRoot = resolve(siteRoot, "src")
const outputPath = resolve(sourceRoot, "lib/lucide-icons.tsrx")

const importPattern =
  /import\s*\{([^}]+)\}\s*from\s*["'][^"']*\/lib\/lucide-icons\.tsrx["']/g

const overrides = {
  Github: [
    [
      "path",
      {
        d: "M15 22v-4a4.8 4.8 0 0 0-1-3.5c3 0 6-2 6-5.5.08-1.25-.27-2.48-1-3.5.28-1.15.28-2.35 0-3.5 0 0-1 0-3 1.5-2.64-.5-5.36-.5-8 0C6 2 5 2 5 2c-.3 1.15-.3 2.35 0 3.5A5.403 5.403 0 0 0 4 9c0 3.5 3 5.5 6 5.5-.39.49-.68 1.05-.85 1.65-.17.6-.22 1.23-.15 1.85v4",
      },
    ],
    ["path", { d: "M9 18c-4.51 2-5-2-7-2" }],
  ],
}

async function main() {
  const sourceFiles = await collectSourceFiles(sourceRoot)
  const iconNames = await collectIconNames(sourceFiles)

  if (iconNames.length === 0) {
    throw new Error("No lucide icon imports found")
  }

  const missing = iconNames.filter((name) => !getIconNode(name))

  if (missing.length > 0) {
    throw new Error(`Missing Lucide icon data: ${missing.join(", ")}`)
  }

  const code = generateModule(iconNames)

  await mkdir(dirname(outputPath), { recursive: true })
  await writeFile(outputPath, code)

  console.log(
    `Generated ${relative(siteRoot, outputPath)} with ${iconNames.length} icons.`,
  )
}

async function collectSourceFiles(directory) {
  const entries = await Array.fromAsync(new Bun.Glob("**/*.{ts,tsrx}").scan(directory))
  return entries
    .filter((entry) => !entry.endsWith("lucide-icons.tsrx"))
    .map((entry) => resolve(directory, entry))
}

async function collectIconNames(files) {
  const names = new Set()

  for (const file of files) {
    const source = await readFile(file, "utf8")

    for (const match of source.matchAll(importPattern)) {
      for (const rawSpecifier of match[1].split(",")) {
        const specifier = rawSpecifier.trim()

        if (!specifier) continue

        const [importedName] = specifier.split(/\s+as\s+/)
        names.add(importedName.trim())
      }
    }
  }

  return Array.from(names).sort((a, b) => a.localeCompare(b))
}

function generateModule(iconNames) {
  const exports = iconNames.map((name) => generateIconExport(name)).join("\n\n")

  return `// Generated by site/scripts/generate-lucide-icons.mjs. Do not edit by hand.\n\n` +
    `type IconProps = {\n` +
    `  class?: string\n` +
    `  className?: string\n` +
    `  size?: string | number\n` +
    `  color?: string\n` +
    `  strokeWidth?: string | number\n` +
    `  absoluteStrokeWidth?: boolean\n` +
    `  title?: string\n` +
    `  children?: any\n` +
    `  [key: string]: any\n` +
    `}\n\n` +
    `function getStrokeWidth(size: string | number, strokeWidth: string | number, absoluteStrokeWidth: boolean) {\n` +
    `  return absoluteStrokeWidth ? (Number(strokeWidth) * 24) / Number(size) : strokeWidth\n` +
    `}\n\n` +
    `${exports}\n`
}

function generateIconExport(name) {
  const iconNode = getIconNode(name)
  const iconClass = toKebabCase(name)
  const nodes = iconNode.map((node) => `    ${serializeNode(node)}`).join("\n")

  return `export function ${name}(props: IconProps) {\n` +
    `  const {\n` +
    `    class: classValue,\n` +
    `    className,\n` +
    `    size = 24,\n` +
    `    color = "currentColor",\n` +
    `    strokeWidth = 2,\n` +
    `    absoluteStrokeWidth = false,\n` +
    `    title,\n` +
    `    children,\n` +
    `    ...rest\n` +
    `  } = props\n` +
    `  const computedStrokeWidth = getStrokeWidth(size, strokeWidth, absoluteStrokeWidth)\n\n` +
    `  return <svg\n` +
    `    {...rest}\n` +
    `    xmlns="http://www.w3.org/2000/svg"\n` +
    `    width={size}\n` +
    `    height={size}\n` +
    `    viewBox="0 0 24 24"\n` +
    `    fill="none"\n` +
    `    stroke={color}\n` +
    `    stroke-width={computedStrokeWidth}\n` +
    `    stroke-linecap="round"\n` +
    `    stroke-linejoin="round"\n` +
    `    class={["lucide-icon", "lucide", "lucide-${iconClass}", classValue, className]}\n` +
    `    {...(!title && !children && { "aria-hidden": "true" })}\n` +
    `  >\n` +
    `    if (title) {\n` +
    `      <title>{title}</title>\n` +
    `    }\n` +
    `${nodes}\n` +
    `    {children}\n` +
    `  </svg>\n` +
    `}`
}

function getIconNode(name) {
  return icons[name] ?? overrides[name]
}

function serializeNode(node) {
  const [tag, attrs] = node
  const serializedAttrs = Object.entries(attrs)
    .map((entry) => {
      const [key, value] = entry
      return `${key}=${JSON.stringify(value)}`
    })
    .join(" ")

  return `<${tag} ${serializedAttrs} />`
}

function toKebabCase(value) {
  return value
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/([A-Z])([A-Z][a-z])/g, "$1-$2")
    .toLowerCase()
}

await main()
